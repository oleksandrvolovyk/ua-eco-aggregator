fun MeteoData.toFullMeteoData(stationId: Int) =
    FullMeteoData(
        stationId,
        getMeteoStationName(stationId),
        getMeteoDataLocation(stationId),
        this.date,
        this.time,
        this.doseInMicroRoentgen,
        this.doseInNanoSievert
